    /// Filled circle
    /// </summary>
    FilledCircle,
    
    /// <summary>
    /// Open circle
    /// </summary>
    OpenCircle,
    
    /// <summary>
    /// Slash mark
    /// </summary>
    Slash,
    
    /// <summary>
    /// Tick mark
    /// </summary>
    Tick
